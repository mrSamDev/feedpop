import "@testing-library/jest-dom/vitest";

// jsdom doesn't implement matchMedia — mock it so useTheme doesn't crash
if (!window.matchMedia) {
  window.matchMedia = (query: string) => ({
    matches: false,
    media: query,
    onchange: null,
    addListener: () => {},
    removeListener: () => {},
    addEventListener: () => {},
    removeEventListener: () => {},
    dispatchEvent: () => false,
  });
}